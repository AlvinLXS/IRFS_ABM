id_max=176
MAX_PARALLEL=30
current_jobs=0
pids=()


# 
for task_id in {1..$id_max}; do
  # 
  (run_task_$task_id) &
  pid=$!
  pids+=("$pid")  # PID
  ((current_jobs++))

  # 
  while ((current_jobs >= MAX_PARALLEL)); do
    for i in "${!pids[@]}"; do
      if ! kill -0 "${pids[$i]}" 2>/dev/null; then
        # PID
        unset 'pids[i]'
        ((current_jobs--))
        break
      fi
    done
    # CPU
    if ((current_jobs >= MAX_PARALLEL)); then
      sleep 0.1
    fi
  done
done

# 
for pid in "${pids[@]}"; do
  wait "$pid" 2>/dev/null
done

